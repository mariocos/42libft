char	*ft_strchr(const char *s, int c)
{
	char	hold;

	hold = c;
	while (*s)
	{
		if (*s == hold)
			return ((const char *)s);
		s++;
	}
	if (hold == '\0')
		return ((char *)s);
	return (0);
}
